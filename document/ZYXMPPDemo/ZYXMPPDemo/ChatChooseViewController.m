- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    
    // Configure the cell...
    if(!cell){
        cell = [[UITableViewCell alloc]initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier];
    }
    cell.textLabel.text = [titleArray objectAtIndex:indexPath.row];
    
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if (indexPath.row==0) {
        SingleChatViewController *singleChatVC = [[SingleChatViewController alloc]initWithNibName:@"SingleChatViewController" bundle:nil];
        [self.navigationController pushViewController:singleChatVC animated:YES];
    }
    if (indexPath.row==1) {
        LoginRoomListViewController *loginRoomsVC = [[LoginRoomListViewController alloc]init];
        [self.navigationController pushViewController:loginRoomsVC animated:YES];
    }
}

